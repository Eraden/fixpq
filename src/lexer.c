static const wchar_t **Lexer_KEYWORDS = NULL;
static const size_t Lexer_KEYWORDS_SIZE = 760;

static void init_keywords();

Lexer *Lexer_init(char *file_path) {
    init_keywords();
    Lexer *tokenizer = (Lexer *) malloc(sizeof(Lexer));
    memset((void *) tokenizer, 0, sizeof(Lexer));
    tokenizer->position.character = 1;
    tokenizer->in = fopen(file_path, "r");
    if (tokenizer->in == NULL) {
        Lexer_free(tokenizer);
        return NULL;
    }
    return tokenizer;
}

void Lexer_free(Lexer *tokenizer) {
    if (tokenizer == NULL)
        return;

    if (tokenizer->in) fclose(tokenizer->in);
    if (tokenizer->buffer) free(tokenizer->buffer);
    if (tokenizer->tokens) {
        for (size_t i = 0; i < tokenizer->tokenLen; i++) {
            LexerToken_free(tokenizer->tokens[i]);
        }
    }
    free(tokenizer);
}

static void LexerToken_free(LexerToken *token) {
    if (token->str) free(token->str);
    free(token);
}

int Lexer_tokenize(Lexer *tokenizer) {
    if (tokenizer == NULL)
        return 0;

    char *lang = getenv("LANG");
    if (lang) {
        setlocale(LC_ALL, lang);
    } else {
        setlocale(LC_ALL, "en_US.utf8");
    }

    while (is_ok(tokenizer)) {
        wchar_t c = getwc(tokenizer->in);
        tokenizer->position.position += 1;
        if (c < 0)
            break;
        switch (c) {
            case L' ': {
                consume(tokenizer);
                break;
            }
            case L'=':
            case L'+':
            case L'-':
            case L'*':
            case L'/':
            case L'%':
            case L'|':
            case L'&':
            case L';':
            case L'\'':
            case L'\"': {
                if (tokenizer->buffer)
                    consume(tokenizer);
                store_character(tokenizer, c);
                consume(tokenizer);
                break;
            }
            case L'\n': {
                if (tokenizer->buffer)
                    consume(tokenizer);
                store_character(tokenizer, c);
                consume(tokenizer);
                tokenizer->position.character = 1;
                tokenizer->position.line += 1;
                break;
            }
            default: {
                store_character(tokenizer, c);
                break;
            }
        }
    }
    return 1;
}

static void store_character(Lexer *tokenizer, wchar_t c) {
    if (tokenizer->buffer == NULL) {
        tokenizer->buffer = (wchar_t *) malloc(sizeof(wchar_t) * 2);
        memset(tokenizer->buffer, 0, 2);
        tokenizer->buffer[0] = c;
        tokenizer->buffer[1] = 0;
    } else {
        size_t len = wcslen(tokenizer->buffer) + 2;
        tokenizer->buffer = (wchar_t *) realloc(tokenizer->buffer, sizeof(wchar_t) * len);
        tokenizer->buffer[len - 2] = c;
        tokenizer->buffer[len - 1] = 0;
    }
    tokenizer->position.character += 1;
}

static void consume(Lexer *tokenizer) {
    LexerToken *token = (LexerToken *) malloc(sizeof(LexerToken));
    memset(token, 0, sizeof(LexerToken));

    if (is_keyword(tokenizer)) {
        token->type = LexerType_Keyword;
    } else if (is_number(tokenizer)) {
        token->type = LexerType_Literal;
    } else if (is_operator(tokenizer)) {
        token->type = LexerType_Operator;
    } else {
        token->type = LexerType_Identifier;
    }
    size_t bufLen = tokenizer->buffer ? wcslen(tokenizer->buffer) : 0;
    token->str = tokenizer->buffer;
    token->position.character = tokenizer->position.character - bufLen;
    token->position.position = tokenizer->position.position - bufLen;
    token->position.line = tokenizer->position.line;

    if (tokenizer->tokens == NULL) {
        tokenizer->tokens = (LexerToken **) malloc(sizeof(LexerToken *) * 1);
        memset(tokenizer->tokens, 0, sizeof(LexerToken *));
        tokenizer->tokens[0] = token;
        tokenizer->tokenLen = 1;
    } else {
        size_t len = tokenizer->tokenLen + 1;
        tokenizer->tokens = realloc(tokenizer->tokens, sizeof(LexerToken *) * len);
        tokenizer->tokens[tokenizer->tokenLen] = token;
        tokenizer->tokenLen += 1;
    }
    tokenizer->buffer = NULL;
}

static int is_keyword(Lexer *tokenizer) {
    if (tokenizer->buffer == NULL)
        return 0;
    for (size_t i = 0; i < Lexer_KEYWORDS_SIZE; i++) {
        if (wcscmp(tokenizer->buffer, Lexer_KEYWORDS[i]) == 0)
            return 1;
    }
    return 0;
}

static int is_number(Lexer *tokenizer) {
    wchar_t *b = tokenizer->buffer;
    if (b == NULL) return 0;
    size_t len = wcslen(b);
    short hadDot = 0;
    for (size_t i = 0; i < len; i++) {
        wchar_t wc = b[i];
        char c = (char) wc;
        if (!isdigit(c) && wc != L'.') {
            return 0;
        } else if (wc == L'.' && hadDot == 0) {
            hadDot = 1;
        } else if (wc == L'.' && hadDot == 1) {
            return 0;
        }
    }
    return 1;
}

static int is_operator(Lexer *tokenizer) {
    wchar_t *b = tokenizer->buffer;
    if (b == NULL) return 0;
    size_t len = wcslen(b);
    if (len > 1) return 0;
    if (len < 1) return 0;
    switch (*b) {
        case L'=':
        case L'+':
        case L'-':
        case L'*':
        case L'/':
        case L'%':
        case L'|':
        case L'&':
        case L';':
            return 1;
        default:
            return 0;
    }
}

static short is_ok(Lexer *tokenizer) {
    if (feof(tokenizer->in)) return 0;
    if (ferror(tokenizer->in)) return 0;
    return 1;
}

